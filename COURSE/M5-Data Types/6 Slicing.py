name = "PYTHONPROGRAMMING"

print(name)

# Syntax
# variable[start:end]

print(name[0:6])

print(name[6:17])

# Negative indexing
print(name[-5:])

# Skipping characters
print(name[0:10:2])

# List slicing
numbers = [10, 20, 30, 40, 50, 60]

print(numbers[1:4])

print(numbers[:3])

print(numbers[3:])

# Reversing
print(numbers[::-1])