# List
a = [1, 2, 3]

# Tuple
b = (1, 2, 3)

# Set
c = {1, 2, 3}

# Dictionary
d = {

    "name": "Madhav",

    "age": 21
}

print(type(a))

print(type(b))

print(type(c))

print(type(d))

# Comparison table

print("\n===== COMPARISON =====")

print("List -> Ordered, Mutable")

print("Tuple -> Ordered, Immutable")

print("Set -> Unordered, No Duplicates")

print("Dictionary -> Key Value Pair")

