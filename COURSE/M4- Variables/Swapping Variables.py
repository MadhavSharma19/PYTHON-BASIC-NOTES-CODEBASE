# =========================================
# Topic : Swapping Variables
# =========================================

a = 10
b = 20

print("Before Swapping")

print("a =", a)

print("b =", b)

# Swapping
a, b = b, a

print("After Swapping")

print("a =", a)

print("b =", b)

# Real life example

tea = "Hot"

coffee = "Cold"

tea, coffee = coffee, tea

print("Tea =", tea)

print("Coffee =", coffee)