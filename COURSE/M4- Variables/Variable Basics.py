# Variable is used to store data

name = "Madhav"

age = 21

height = 5.9

# Printing variables
print("Name =", name)

print("Age =", age)

print("Height =", height)

# Variables can change values
age = 22

print("Updated Age =", age)

# Python automatically detects datatype
print(type(name))

print(type(age))

print(type(height))

# Real life example
pizza_price = 299

quantity = 3

total = pizza_price * quantity

print("Total Bill =", total)