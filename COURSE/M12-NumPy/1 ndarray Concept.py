# ndarray means N-Dimensional Array

import numpy as np

# Creating numpy array
a = np.array([10, 20, 30, 40])

print(a)

# Type of array
print(type(a))

# Dimensions
print(a.ndim)

# Shape
print(a.shape)

# Datatype
print(a.dtype)

# Size
print(a.size)