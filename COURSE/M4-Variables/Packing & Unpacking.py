# Packing
# Multiple values stored in one variable

a = ("Python", "Java", "C++")

print(a)

# Unpacking
# Extracting values

x, y, z = a

print(x)

print(y)

print(z)

# Real life example

student = ("Madhav", 21, "Python")

name, age, course = student

print("Name =", name)

print("Age =", age)

print("Course =", course)